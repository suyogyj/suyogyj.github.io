// Animation on scroll
document.addEventListener('DOMContentLoaded', () => {
    const elements = document.querySelectorAll('.skill-item, .project-item, .timeline-item, .contact-item, .interest-item, .publication-item');
    
    // Initialize elements with opacity 0 and transform for animations
    elements.forEach(element => {
        element.style.opacity = '0';
        element.style.transform = 'translateY(20px)';
        element.style.transition = 'opacity 0.5s ease, transform 0.5s ease';
    });
    
    // Function to check if element is in viewport and animate
    const handleScroll = () => {
        elements.forEach(element => {
            const elementPosition = element.getBoundingClientRect().top;
            const windowHeight = window.innerHeight;
            
            if (elementPosition < windowHeight - 100) {
                element.style.opacity = '1';
                element.style.transform = 'translateY(0)';
            }
        });
    };
    
    // Trigger scroll event to check for visible elements on page load and during scroll
    window.addEventListener('scroll', handleScroll);
    handleScroll(); // Initial check
});
